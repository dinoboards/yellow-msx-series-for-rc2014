	; LD	DE, MSG.CPU_SPEED
	; CALL	PRINT

	; LD	DE, MSG.MEMORY
	; CALL	PRINT

	; LD	DE, MSG.VIDEO
	; CALL	PRINT

	LD	DE, MSG.SIO
	CALL	PRINT

	CALL	SIO_PROBE
	JR	Z, SIO_FOUND

	LD	DE, MSG.NOT
	CALL	PRINT

SIO_FOUND:
	LD	DE, MSG.SIO_PRESENT
	CALL	PRINT

	LD	DE, MSG.RTC
	CALL	PRINT

	CALL	RP5RTC_PROBE
	JR	Z, RTC_FOUND

	LD	DE, MSG.NOT
	CALL	PRINT

RTC_FOUND:
	LD	DE, MSG.PRESENT
	CALL	PRINT

	; LD	DE, MSG.GAME
	; CALL	PRINT

	RET

